# Arithmetic Operator

print(10 + 3)
print(10 // 3)
print(10 ** 3)
print(10 % 3)

# Operator Precedense

x = 10 + 3 * 2
print(x)
y = (10 + 3) * 2
print(y)

# Comparison Operators (<, >, ==, <=, >=, != )

x = 3 > 2
y = 3 <= 2
z = 3 == 2
a = 1 != 2
print(x, y, z, a)

# Logical Operators (AND OR NOT)

price = 20
print(price < 10 and price < 30)

income = 100
print(income > 200 or income < 1000)

num = 50
print(not num > 10)
